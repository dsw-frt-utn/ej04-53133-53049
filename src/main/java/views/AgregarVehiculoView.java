    @Override
public void setVisible(boolean visible) {
    if (visible) cargarDatos();
    super.setVisible(visible);
}

private void cargarDatos() {
    // Cargar marcas
    cmbMarca.removeAllItems();
    for (domain.Marca m : data.Persistencia.getMarcas()) {
        cmbMarca.addItem(m.getNombre());
    }
    // Cargar sucursales
    cmbSucursal.removeAllItems();
    for (domain.Sucursal s : data.Persistencia.getSucursales()) {
        cmbSucursal.addItem(s.getCodigo());
    }
    // Cargar tipos
    cmbTipo.removeAllItems();
    cmbTipo.addItem("ELECTRICO");
    cmbTipo.addItem("COMBUSTIBLE");

    actualizarCampos();
}

private void actualizarCampos() {
    boolean esElectrico = "ELECTRICO".equals(cmbTipo.getSelectedItem());
    lblParam1.setText(esElectrico ? "kWh base:" : "Km por litro:");
    lblParam2.setText("Litros extra:");
    txtParam2.setVisible(!esElectrico);
    lblParam2.setVisible(!esElectrico);
}
    
    
    
    
    
