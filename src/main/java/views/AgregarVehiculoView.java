    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
    try {
        String patente = txtPatente.getText().trim();
        String modelo = txtModelo.getText().trim();
        int anio = Integer.parseInt(txtAnio.getText().trim());
        double capacidad = Double.parseDouble(txtCapacidad.getText().trim());
        double param1 = Double.parseDouble(txtParam1.getText().trim());
        double param2 = txtParam2.isVisible() ? Double.parseDouble(txtParam2.getText().trim()) : 0;

        // Obtener marca seleccionada
        int idxMarca = cmbMarca.getSelectedIndex();
        domain.Marca marca = data.Persistencia.getMarcas().get(idxMarca);

        // Obtener sucursal seleccionada
        int idxSuc = cmbSucursal.getSelectedIndex();
        domain.Sucursal sucursal = data.Persistencia.getSucursales().get(idxSuc);

        // Obtener tipo
        domain.VehiculoTipo tipo = domain.VehiculoTipo.valueOf((String) cmbTipo.getSelectedItem());

        Controlador.agregarVehiculo(patente, marca, modelo, anio, capacidad, sucursal, tipo, param1, param2);

        javax.swing.JOptionPane.showMessageDialog(this, "Vehículo agregado correctamente.");
        limpiarFormulario();

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingresá valores numéricos válidos.");
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

